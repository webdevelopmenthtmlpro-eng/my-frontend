// Ensure every language has all keys: fill missing entries from English as fallback
window.translations = window.translations || {};
(function ensureCompleteTranslations() {
  const en = window.translations.en || {};
  const languages = Object.keys(window.translations);
  languages.forEach((lang) => {
    if (lang === 'en') return;
    window.translations[lang] = window.translations[lang] || {};
    Object.keys(en).forEach((key) => {
      if (!(key in window.translations[lang])) {
        window.translations[lang][key] = en[key];
      }
    });
  });
})();
